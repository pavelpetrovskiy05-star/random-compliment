# main.py
import random

# Цвета для терминала
colors = ["\033[91m", "\033[92m", "\033[93m", "\033[94m", "\033[95m", "\033[96m"]
reset = "\033[0m"

# Ввод имени
name = input("Введите ваше имя: ")

# Комплименты по категориям
compliments = {
    "внешность": [
        "Ты сегодня прекрасно выглядишь!",
        "Твоя улыбка заряжает позитивом!",
        "Ты сияешь красотой!"
    ],
    "характер": [
        "Ты очень добрый человек!",
        "У тебя замечательный характер!",
        "Ты всегда поддерживаешь других!"
    ],
    "таланты": [
        "Ты очень талантливый человек!",
        "Ты умеешь вдохновлять окружающих!",
        "Твои способности впечатляют!"
    ]
}

# Выбор категории
print("\nВыберите категорию комплиментов:")
print("1 - внешность")
print("2 - характер")
print("3 - таланты")
choice = input("Введите номер категории: ")

if choice == "1":
    category = "внешность"
elif choice == "2":
    category = "характер"
elif choice == "3":
    category = "таланты"
else:
    category = random.choice(list(compliments.keys()))

# Рандомный комплимент и цвет
compliment = random.choice(compliments[category])
color = random.choice(colors)

# Вывод
print(f"\nПривет, {name}! Вот твой комплимент ({category}):")
print(f"{color}{compliment}{reset}")
